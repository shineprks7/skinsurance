function shapeAnimationCombine (fromShape, multiShapes)
{
const width = 1000;

const height =500;

const shapeListArray = flubber.splitPathString(multiShapes);





   const path =    d3.select("#slider_animated_path");




    const interpolationstring = flubber.combine( shapeListArray ,fromShape, {single:true});

    path.transition().duration(300)
                      .attrTween("d", ( d )  =>  t => interpolationstring(t))
                      .on("end", function()
                      {
                        console.log("animation Completed");

                      }

                        
                      );

                     
}

